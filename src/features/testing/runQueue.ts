/**
 * Последовательная очередь прогонов тестов
 *
 * Прогоны нельзя выполнять параллельно: все фреймворки, кроме OneScript,
 * работают с одной информационной базой, и параллельные сеансы 1С
 * блокируют друг друга. Очередь выстраивает задачи в промис-цепочку.
 */
export class RunQueue {
	private tail: Promise<void> = Promise.resolve();

	/**
	 * Ставит задачу в очередь
	 *
	 * Задача начнёт выполняться после завершения всех ранее поставленных.
	 * Ошибка задачи не ломает очередь — следующие задачи выполнятся.
	 *
	 * @param job - Асинхронная задача
	 * @returns Промис завершения именно этой задачи
	 */
	public enqueue(job: () => Promise<void>): Promise<void> {
		const result = this.tail.then(job);
		// Глушим ошибку в хвосте, чтобы цепочка продолжала жить
		this.tail = result.catch(() => undefined);
		return result;
	}
}
