/**
 * Типы результатов разбора тестовых файлов
 *
 * Чистые структуры без зависимостей от vscode — парсеры работают
 * со строками и тестируются юнит-тестами без Extension Host.
 */

/**
 * Обнаруженный тестовый кейс (сценарий или тест-метод)
 */
export interface DiscoveredCase {
	/** Имя сценария / тест-метода (как ожидается в jUnit testcase name) */
	name: string;
	/** Строка объявления, 0-based — для TestItem.range и гуттер-кнопки */
	line: number;
	/** Теги Gherkin (@...) без символа @ */
	tags?: string[];
	/**
	 * Имя метода-процедуры для точечного запуска, если оно отличается от name.
	 *
	 * У параметризованных тестов name — это отображаемое имя набора значений
	 * (напр. «[ibcmd]»), совпадающее с jUnit testcase name, а запускать раннеру
	 * нужно саму процедуру (oneunit `-m <методName>`). Для обычных кейсов не задаётся
	 * (метод и так равен name).
	 */
	methodName?: string;
	/**
	 * Имя группы (контейнера) параметризованного теста — отображаемое имя процедуры.
	 *
	 * В jUnit OneUnit одноимённые наборы значений разных процедур («[ibcmd]») лежат
	 * во вложенных testsuite с этим именем. Служит и для различения одноимённых кейсов
	 * при сопоставлении, и как поясняющая подпись в дереве. Для обычных кейсов пусто.
	 */
	groupName?: string;
}

/**
 * Результат разбора тестового файла
 */
export interface DiscoveredFile {
	/** Заголовок файла (имя Функционала или имя модуля); пусто — берётся имя файла */
	label?: string;
	/** Строка заголовка файла, 0-based */
	labelLine?: number;
	/** Обнаруженные кейсы */
	cases: DiscoveredCase[];
}
